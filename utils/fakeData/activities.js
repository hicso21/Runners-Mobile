function generateRunsData() {
	const today = new Date(new Date().toLocaleDateString()).getTime();
	const oneDayTimestamp = 86400000;
	const runs = [];
	for (let i = 0; i < 365; i++) {
		const average_heart_rate =
			Math.floor(Math.random() * (140 - 110 + 1)) + 110;
		const average_pace = (Math.random() * (8 - 6 + 1)).toFixed(2);
		const average_cadence = Math.floor(Math.random() * (90 - 70 + 1)) + 70;
		const min_height = Math.floor(Math.random() * (200 - 100 + 1)) + 100;

		runs.push({
			_id: '',
			user_id: '',
			title: '',
			date: new Date(today - i * oneDayTimestamp).toLocaleDateString(),
			timestamp: Date.now() - 604800 * i,
			distance: Math.floor(Math.random() * (50 - 20 + 1)) + 20,
			total_time: Math.floor(Math.random() * (180 - 120 + 1)) + 120,
			average_heart_rate,
			max_heart_rate:
				Math.floor(Math.random() * (10 + average_heart_rate)) +
				average_heart_rate,
			resting_heart_rate: '',
			average_pace,
			calories: Math.floor(Math.random() * (7000 - 4000 + 1)) + 4000,
			training_load: '',
			positive_slope: '',
			negative_slope: '',
			average_speed: (1 / parseFloat(average_pace)).toFixed(2),
			average_cadence,
			max_cadence:
				Math.floor(Math.random() * (20 + average_cadence)) +
				average_cadence,
			min_height,
			max_height: Math.floor(Math.random() * (min_height + 500)),
			estimated_liquid_loss: '',
			average_temperature: Math.floor(Math.random() * (30 - 10 + 1)) + 10,
			paces: [],
			triathlonData: [],
			description: '',
		});
	}
	return runs.reverse();
}

